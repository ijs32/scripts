#!/bin/bash
set -e

echo "========================================================="
echo "UPDATING APT REPOSITORY"
echo "========================================================="
sudo apt update -y || true
echo "========================================================="
echo "ISSUING A FULL-UPGRADE"
echo "========================================================="
sudo apt full-upgrade
echo "========================================================="
echo "AUTOREMOVING UN-NEEDED PACKAGES"
echo "========================================================="
sudo apt autoremove 
echo "========================================================="
echo "CLEANING THINGS UP..."
echo "========================================================="
sudo apt clean
sudo apt autoclean
echo "========================================================="
echo "ALL DONE!"
echo "========================================================="
